import sys
import os
import shutil
from datetime import date

sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), '..')))
from src.inverted_index import LSMInvertedIndex

def run_comprehensive_tests():
    data_dir = "./data"
    # Очищаем базу
    if os.path.exists(data_dir):
        shutil.rmtree(data_dir)
    os.makedirs(data_dir)

    print("=== Инициализация системы информационного поиска ===")
    index = LSMInvertedIndex(data_dir=data_dir, flush_threshold=5)

    documents = [
        (1, "Основы 3D печати фигурок лего и калибровка принтера FlashForge", date(2024, 3, 1), date(2024, 3, 1), None),
        (2, "Технический регламент Формулы-1 на 2026 год", date(2024, 4, 15), date(2024, 1, 1), date(2026, 12, 31)),
        (3, "Использование Roaring Bitmaps в инвертированных индексах", date(2024, 4, 20), date(2024, 4, 20), None),
        (4, "Алгоритмы калибровки экструдера для качественной 3D печати", date(2024, 2, 10), date(2024, 2, 10), date(2024, 5, 1)),
        (5, "Макс Ферстаппен и Юки Цунода: успехи в текущем сезоне Ф1", date(2024, 4, 10), date(2024, 4, 10), None),
        (6, "Разработка космических игр на Unity: введение в 2D изометрию", date(2024, 4, 25), date(2024, 4, 25), None),
    ]

    print(f"Добавление {len(documents)} документов...\n")
    for doc in documents:
        index.add_document(doc_id=doc[0], text=doc[1], created_at=doc[2], valid_from=doc[3], valid_to=doc[4])

    # ==========================================
    # ЗАДАНИЕ 2: Булев поиск (AND)
    # ==========================================
    print("--- ТЕСТ ЗАДАНИЯ 2: Булев поиск (AND) ---")
    
    # Тест 2.1: Поиск одного слова 
    res2_1 = index.search_boolean_and("калибровка")
    print(f"Тест 2.1 (Множественное совпадение) | 'калибровка': {list(res2_1)} (Ожидаем [1, 4])")
    
    # Тест 2.2: Пересечение двух слов 
    res2_2 = index.search_boolean_and("калибровка алгоритмы")
    print(f"Тест 2.2 (Пересечение AND)          | 'калибровка алгоритмы': {list(res2_2)} (Ожидаем [4])")
    
    # Тест 2.3: Негативный сценарий 
    res2_3 = index.search_boolean_and("блокчейн")
    print(f"Тест 2.3 (Негативный сценарий)      | 'блокчейн': {list(res2_3)} (Ожидаем [])\n")

    # ==========================================
    # ЗАДАНИЕ 3A: Поиск по префиксу
    # ==========================================
    print("--- ТЕСТ ЗАДАНИЯ 3A: Префиксный поиск ---")
    
    # Тест 3A.1: Общий префикс для нескольких документов
    res3a_1 = index.search_prefix("кал") # найдет "калибровка" и "калибровки"
    print(f"Тест 3A.1 (Множественное совпадение)| Префикс 'кал*': {list(res3a_1)} (Ожидаем [1, 4])")
    
    # Тест 3A.2: Узкий префикс для одного документа
    res3a_2 = index.search_prefix("индекс")
    print(f"Тест 3A.2 (Одно совпадение)         | Префикс 'индекс*': {list(res3a_2)} (Ожидаем [3])")
    
    # Тест 3A.3: Негативный сценарий
    res3a_3 = index.search_prefix("астро")
    print(f"Тест 3A.3 (Негативный сценарий)     | Префикс 'астро*': {list(res3a_3)} (Ожидаем [])\n")

    # ==========================================
    # ЗАДАНИЕ 3B: Поиск по wildcard (k-grams)
    # ==========================================
    print("--- ТЕСТ ЗАДАНИЯ 3B: Wildcard поиск (k-grams) ---")
    
    # Тест 3B.1: Поиск по корню слова в середине
    res3b_1 = index.search_wildcard("*либро*") # внутри каЛИБРОвка
    print(f"Тест 3B.1 (Кусок слова внутри)      | Wildcard '*либро*': {list(res3b_1)} (Ожидаем [1, 4])")
    
    # Тест 3B.2: Поиск по другому корню
    res3b_2 = index.search_wildcard("*глам*") # внутри реГЛАМент
    print(f"Тест 3B.2 (Единичное совпадение)    | Wildcard '*глам*': {list(res3b_2)} (Ожидаем [2])")
    
    # Тест 3B.3: Негативный сценарий
    res3b_3 = index.search_wildcard("*квант*")
    print(f"Тест 3B.3 (Негативный сценарий)     | Wildcard '*квант*': {list(res3b_3)} (Ожидаем [])\n")

    # ==========================================
    # ЗАДАНИЕ 4: Метаданные (Даты) и Булева логика
    # ==========================================
    print("--- ТЕСТ ЗАДАНИЯ 4: Даты + Булева логика ---")
    
    # Тест 4.1: Поиск появившихся в заданном диапазоне
    res4_1 = index.get_date_range_bitmap(date(2024, 4, 1), date(2024, 4, 30))
    print(f"Тест 4.1 (Фильтр: cозданы в апреле 2024)      : {list(res4_1)} (Ожидаем [2, 3, 5, 6])")
    
    # Тест 4.2: Смешанный поиск (Текст + Валидность по времени)
    # Ищем слово "калибровка", но документ должен быть валиден в 2026 году.
    # Док 1 валиден вечно (до None). Док 4 "умер" в мае 2024.
    text_bit = index.search_boolean_and("калибровка")
    date_bit = index.get_valid_in_range_bitmap(date(2026, 1, 1), date(2026, 12, 31))
    res4_2 = text_bit & date_bit
    print(f"Тест 4.2 (Текст 'калибровка' И валиден в 2026): {list(res4_2)} (Ожидаем [1])")

    print("\n=== Все тесты отработали корректно ===")

if __name__ == "__main__":
    run_comprehensive_tests()
